function get_Λ(β, χ)
    s,c = sincos(χ)
    γ = 1/sqrt(1-β^2)

    Λ00 = γ
    Λ01 = -γ*β*c
    Λ02 = -γ*β*s
    Λ11 = (γ-1)*c^2 +1
    Λ12 = (γ-1)*s*c
    Λ22 = (γ-1)*s^2
    return @SMatrix[Λ00 Λ01 Λ02 0;
                    Λ01 Λ11 Λ12 0;
                    Λ02 Λ12 Λ22 0;
                    0   0   0   1
                   ]
end
